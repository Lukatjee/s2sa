        <h1>Het ontstaan van S2SA (Start 2 Sport Again)</h1>
        <h2>Johan De Groof</h2>
        <p>In mei 2000 kreeg ik een arbeidsongeval, na vele operaties werd eind 2019 mijn onderbeen geamputeerd. Dit was
            een hel op mentaal vlak. Maar de mentale weerbaarheid is een groot deel van onze leefwijze en neemt je leven
            over. Een ander deel is de fysieke beperking welke éénieder onafhankelijk mee moet omgaan. Ik heb
            ondervonden dat het belangrijk is om in beweging te blijven om zo het mentale, fysische en fysieke gedeelte
            van het ons zijn te kunnen in stand houden of verbeteren.
        </p>
        <p>Op fysische en fysieke vlak is er een grote MAAR. Personen met een amputatie beschikken na de amputatie, niet
            over de financiële middelen om de aangepaste uitrusting en sport benodigdheden aan te schaffen.
            Als deze mensen graag aan sport willen doen, moeten zij ook kunnen beschikken over de nodige accomodatie en
            ondersteuning. Voor veel van deze slachtoffers wordt sporten onbetaalbaar daar de sportprotheses niet
            terugbetaald worden door onze ziekenzorg. Deze mensen blijven, aan hun rolstoel gekluisterd, de zin van het
            leven verliezen.
        </p>
        <p>Dit heb ik ook aangekaart bij verschillende revalidatieartsen en zorgverstrekkers.
            Meer en meer mensen weerspiegelen zich in mijn verhaal. Zijn verbaasd hoe ik omga met mijn beenamputatie
            en hopen dat ook zij een beter leven mogen krijgen door aan sport te gaan doen.</p>

        <h2>Joris Schuerwegen</h2>
        <p>Mijn naam is Joris en ik ben kinesitherapeut bij MOTUS in Lille. Reeds 2 jaar geleden heb ik de eer gehad om
            Johan te ontmoeten. Johan heeft helaas een onderbeen moeten laten amputeren als gevolg van een plettrauma en
            was op zoek naar iemand die hem kon helpen bij zijn revalidatie. Via To Walk Again is hij bij mij terecht
            gekomen als zijn kinesitherapeut en heb hem sindsdien begeleid bij zijn revalidatie.
        </p>
        <p>Ikzelf ben sinds 2015 actief als sportkinesitherapeut en manueel therapeut en probeer in mijn vrije tijd een
            goeie papa te zijn voor mijn zoon Jules. Alsook speel ik competitievoetbal bij KFC Grobbendonk, competitie
            padel, probeer ik jaarlijks te skiën en wil ik binnen de 2 jaar een marathon gelopen hebben. Ik heb altijd
            een enorme voldoening gehaald uit het behandelen van complexe revalidaties. Die van Johan was voor mij een
            compleet nieuw terrein, maar tevens ook een super boeiende ervaring. Ik had nog nooit met dergelijke
            patiënten gewerkt. De wilskracht en positiviteit van iemand die zowel fysiek als mentaal al zoveel klappen
            heeft gekregen in het leven, werkte ontzettend aanstekelijk.
        </p>
        <p>
            Tijdens onze sessies kwamen we erachter dat we een gedeelde passie hebben voor het helpen van mensen. We
            besloten dat we samen een vzw wilden oprichten om patiënten met een amputatie te ondersteunen en te helpen
            bij het vinden van de juiste informatie, praktische hulp en vooral financiële steun bij aankoop van vaak
            heel dure prothesen.
        </p>
        <p>
            Via onze vzw trachten we voornamelijk projecten te ondersteunen die mensen in staat stellen om te sporten en
            hun activiteitenniveau weer kunnen oppakken. Dit is niet alleen fysiek belangrijk, maar zeker ook mentaal.
            Door te sporten kunnen mensen hun zelfvertrouwen terugwinnen, hun grenzen verleggen en hun algemene
            gezondheid verbeteren. Bij Johan is dit een absolute noodzaak gebleken om zijn leven opnieuw zin te geven en
            opnieuw ‘quality of life’ te vinden. Het is mijn missie om samen met Johan anderen te helpen hun weg te
            vinden in het leven na een amputatie en te laten zien dat er nog steeds veel mogelijkheden zijn om actief en
            gelukkig te leven.

        </p>
        <h2>Stijn Lecleir</h2>
        <p>Stijn "Popeye" Lecleir (41) is geboren met arthrogryposis (AMC), een aandoening aan de spieren en gewrichten
            die zorgen voor een standafwijking en bewegingsbeperking. Bij Stijn zijn de spieren in de onderste ledematen
            onderontwikkeld. Zijn bovenste ledematen en romp zijn niet aangetast en heeft Stijn doorheen zijn hele leven
            volop ontwikkeld en getraind.
        </p>
        <p>Stijn traint in het handbikeseizoen, van november tot juni, wekelijks met vzw Join2Bike. Ritten van dertig
            kilometer of langer zijn geen uitzondering ter voorbereiding op de jaarlijkse reis naar de Oostenrijkse
            bergen. Zo fietste Stijn ondermeer al de Stelvio op.
        </p>
        <p>
            "Voor de uitdaging in 2025 zal ik toch mijn kuiten mogen insmeren. Met de wekelijkse training zal ik er niet
            komen maar dat is net wat me aanspreekt: mijn grens verleggen en iets unieks verwezenlijken", aldus Stijn.
        </p>
        <p>
            Het wordt niet de eerste keer dat Stijn traint voor een sportief doel. Zo zwom hij nog de selecties voor de
            Paralympische Spelen van 2000 in Sydney en dit zowel op de 50 meter, 100 meter als 400 meter crawl. "Ik weet
            wat het is om door te zetten en die focus volop te leggen op dat ene doel. Met de financiële en
            organisatorische steun van S2SA wil ik deze uitdaging aangaan."
        </p>
        <p>
            Naast de handbike is Stijn een goedlachse schoenmaker uit Mechelen. Werken doet hij in Leuven bij Orthopedie
            Van Haesendonck waar hij orthopedische schoenen maakt en herstelt. Het zijn vooral de variatie en de leuke
            collega's die ervoor zorgen dat Stijn zijn job elke dag met de glimlach doet.
        </p>
