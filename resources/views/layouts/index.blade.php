        <h1>Hoe is start to sport again ontstaan?</h1>

        <p class="lead">Als er iets is dat ik de voorbije jaren heb geleerd, is dat er altijd hoop moet zijn. En
            perspectief … en
            dat is exact wat we met de vzw Start To Sport Again (S2SA) willen bieden. Wij ondersteunen immers mensen met
            een fysieke beperking in hun weg naar een actief leven waar ze ten volle van kunnen genieten.</p>
        <p>Ik heb het letterlijk aan den lijve ondervonden want in mei 2000 kreeg ik een arbeidsongeval en na vele
            operaties werd eind 2019 mijn onderbeen geamputeerd. Het was een hel op mentaal vlak, maar terwijl je moet
            leren omgaan met je fysieke beperking, stelde ik toch ook vast dat mentale weerbaarheid een groot deel van
            onze leefwijze is en uiteindelijk zelfs je leven kan overnemen.</p>
        <p>Ik leerde dat het belangrijk is om in beweging te blijven om zo het mentale, fysische en fysieke gedeelte
            van ons zijn in stand te kunnen houden of te verbeteren. Niet eenvoudig want op het fysische en fysieke vlak
            is er een grote maar: personen met een beenamputatie, hebben na amputatie, via het ziekenfonds wel recht op
            één kunstbeen (mits een oplegging van duizenden euro's). Maar wanneer deze mensen aan sport willen doen,
            moeten zij ook kunnen beschikken over een dure sportprothese. Voor velen van deze lotgenoten is zo'n
            onbetaalbaar waarbij ik zelf vaststel dat deze mensen aan hun rolstoel gekluisterd zijn en zo de zin van het
            leven verliezen.</p>
        <p>Deze onrechtvaardigheid heb ik als ervaringsdeskundige al aangekaart bij verschillende revalidatieartsen en
            zorgverstrekkers. Tegelijk trekken lotgenoten zich op aan mijn verhaal en zijn ze verbaasd hoe ik omga met
            mijn beenamputatie. Zij hopen dat ook zij een beter leven mogen (terug)krijgen door aan sport te gaan
            doen.</p>

        <h2>Doelstelling S2SA</h2>
        <p>Het doel van de vzw is om fondsen op te halen, uit giften, sponsoring en activiteiten. Met deze fondsen
            kunnen we mensen een sportprothese schenken of alleszins de kostprijs flink verlagen. Hierdoor krijgen ze
            opnieuw de mogelijkheid om meer te bewegen wat hun mentale, fysische en fysieke gezondheid en algemeen
            welzijn ongetwijfeld ten goede komt. Ons objectief is voorlopig om voldoende fondsen te verzamelen om
            minstens één sportprothese per jaar te financieren.</p>
        <p>Tegelijk wil S2SA het probleem van de prijs van de prothese en de gevolgen hiervan onder de aandacht van de
            overheid brengen.</p>

        <h2>Project 2024</h2>
        <p>Voor 2024 hebben we alvast twee projecten voor ogen. Sarah uit Hemiksem is een jonge vrouw die, in
            dezelfde periode als ik, een onderbeenamputatie moeten ondergaan door een vaatprobleem en problemen met de
            aderen in haar been. Zij was een fanatieke hardloopster en is intussen zowat de moed verloren om nog te gaan
            sporten, precies omdat een sportprothese veel te duur is en er geen enkele tussenkomst voorzien is.</p>

        <p>S2SA wil zich inzetten opdat Sarah wel terug kan gaan sporten. Ik heb alvast haar weemoed gekend maar
            heb ook de lach terug op haar gezicht gezien wanneer we gingen zeggen dat er toch hoop is. En perspectief.
            In de zomer van 2024 ga ikzelf alvast de uitdaging aan om via strava de Mont Ventoux te beklimmen en me
            hierbij per kilometer te laten sponsoren. In de hoop dat dit iniatief voldoende fondsen kan opleveren zodat
            we Sarah haar prothese kunnen schenken en ze weer aan sporten kan denken. Het kan onze eerste schenking
            zijn, waarna er hopelijk nog veel volgen.</p>
